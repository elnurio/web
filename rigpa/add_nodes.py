# ── Helpers ───────────────────────────────────────────────────────────────────

def js_node_str(n):
    """Convert a node dict to the JS object literal format used in mock.js."""
    lines = []
    related_js = '[' + ', '.join("'" + r + "'" for r in n['relatedTo']) + ']'
    lines.append("  {")
    lines.append(f"    id: '{n['id']}', label: '{n['label']}', tibetan: '{n['tibetan']}',")
    lines.append(f"    category: '{n['category']}', weight: {n['weight']}, color: '{n['color']}',")
    lines.append(f"    relatedTo: {related_js}")
    lines.append("  }")
    return '\n'.join(lines)


# ── Process mock.js ───────────────────────────────────────────────────────────

with open(MOCK_JS, 'r', encoding='utf-8') as f:
    js_content = f.read()

# Find existing IDs in JS file
existing_js_ids = set(re.findall(r"id:\s*'([^']+)'", js_content))

nodes_to_add_js = [n for n in NEW_NODES if n['id'] not in existing_js_ids]

if nodes_to_add_js:
    # Insert before the closing `];` that ends _NODES_DATA.
    # We look for the pattern `\n];\n\n// ── Автосборка` which uniquely identifies
    # the end of the _NODES_DATA array (followed by the KNOWLEDGE_GRAPH comment).
    insert_marker = '\n];\n\n// ── Автосборка'
    pos = js_content.find(insert_marker)
    if pos == -1:
        # Fallback: look for any `// ── Автосборка` line and back-find `];`
        pos_auto = js_content.find('// ── Автосборка')
        if pos_auto == -1:
            print("ERROR: Could not find _NODES_DATA end marker in mock.js", file=sys.stderr)
            sys.exit(1)
        # Find the `];` just before `// ── Автосборка`
        pos = js_content.rfind('\n];', 0, pos_auto)
        if pos == -1:
            print("ERROR: Could not find `];` before Автосборка in mock.js", file=sys.stderr)
            sys.exit(1)
    # pos points to `\n];\n\n// ── Автосборка` or just `\n];`
    # We insert before the `\n];` part
    new_js_blocks = ',\n'.join(js_node_str(n) for n in nodes_to_add_js)
    js_content = js_content[:pos] + ',\n' + new_js_blocks + js_content[pos:]

    with open(MOCK_JS, 'w', encoding='utf-8') as f:
        f.write(js_content)
    print(f"mock.js: added {len(nodes_to_add_js)} new nodes.")
else:
    print("mock.js: no new nodes to add (all IDs already present).")

